package com.flowerexpress.Models;

public class WhatsNewData {

    private String image;
    private String name;
    private String path;
    private String id;
    private String variationMin;
    private String variationMax;

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getVariationMin() {
        return variationMin;
    }

    public void setVariationMin(String variationMin) {
        this.variationMin = variationMin;
    }

    public String getVariationMax() {
        return variationMax;
    }

    public void setVariationMax(String variationMax) {
        this.variationMax = variationMax;
    }
}
